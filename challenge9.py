print('\nNumber of hours in',numberOfDays,'days are', numberOfDays*24)
print('\nNumber of minutes in',numberOfDays,'days are', numberOfDays*24*60)
print('\nNumber of hours in',numberOfDays,'days is', numberOfDays*24*24*60*60)